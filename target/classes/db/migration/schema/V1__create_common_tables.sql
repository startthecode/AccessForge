CREATE SEQUENCE IF NOT EXISTS gender_ID
    START WITH 1
    INCREMENT BY 5;

CREATE SEQUENCE IF NOT EXISTS status_id
    START WITH 1
    INCREMENT BY 5;

CREATE TABLE IF NOT EXISTS gender (
    id BIGINT PRIMARY KEY DEFAULT nextval('gender_ID'),
    gender VARCHAR(32) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS status (
    id BIGINT PRIMARY KEY DEFAULT nextval('status_id'),
    status VARCHAR(255) NOT NULL UNIQUE
);
